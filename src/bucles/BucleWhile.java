package bucles;

/**
 * while loop
 * Qué es un bucle while y para que se usa.
 *  Nueva clase y método “main”
 *  Estructura del bucle “while”
 *  Bucles infinitos y condición de salida
 *  Comprobar bucle infinito
 */
public class BucleWhile {
    public static void main(String[] args) {
// se evalua una condiciones  y metodos, se usa contadores

     int count =0;
     while (count<10){
         System.out.println("hola mundo  "+count);
         count++;
     }
        System.out.println("fin");
    }
}
