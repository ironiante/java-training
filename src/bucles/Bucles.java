package bucles;

/**
 * foorloop
 * Qué son los bucles for y como utilizarlos.
 *  Nueva clase y método “main”
 *  Sintaxis del bucle “for”
 *  Imprimir un texto varias veces
 *  Cambio de operador
 *  Depuración de código
 *  Cuándo usarlos
 *  Alterar el bucle
 */
public class Bucles {
    public static void main(String[] args) {
// condicion de comienzo donde esta i y comienza en cero
        //condicion de parada
        // condicion de paso, como se incrementa ese bucle
        // vamos a imprimir un texto 10 veces
        for (int i = 0; i < 10; i++) {
            System.out.println( i  +   "hola mundo");
        }
    }
}